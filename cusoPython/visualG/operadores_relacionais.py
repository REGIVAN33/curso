numero_1 = int(input("Digite um número "))
numero_2 = int(input("Digite outro número "))


resuldado = numero_1 == numero_2
print(f"{numero_1} == {numero_2} {resuldado}")

resuldado = numero_1 != numero_2
print(f"{numero_1} != {numero_2} {resuldado}")

resuldado = numero_1 > numero_2
print(f"{numero_1} > {numero_2} {resuldado}")

resuldado = numero_1 < numero_2
print(f"{numero_1} < {numero_2} {resuldado}")

resuldado = numero_1 >= numero_2
print(f"{numero_1} >= {numero_2} {resuldado}")

resuldado = numero_1 <= numero_2
print(f"{numero_1} <= {numero_2} {resuldado}")
