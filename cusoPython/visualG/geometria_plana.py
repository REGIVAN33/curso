# Um aluno iniciou seus estudos em geometria plana e, para
# validar se suas respostas estão corretas, solicitou sua ajuda. Sabendo que Área (triangulo) = (base*altura)/2, construa um programa para
# auxiliar esse aluno.

base = float(input("Digite a Base: "))
