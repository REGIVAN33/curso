# Operadores de atribuição

# '=' Atribuição simples
x = 2
#x recebe 2

# += Atribuição de adição 
x += 2
#equivale a x = x + 2

# -= Atribuição de subtração 
x -= 2
#equivale a x = x – 2

# *= Atribuição de multiplicação
x *= 2
#equivale a x = x * 2

# /= Atribuição de divisão 
x/= 2
#equivale a x = x / 2

# %= Atribuição de resto inteiro da divisão
x%= 2
#equivale a x = x % 2

# **= Atribuição de potência 
x**= 2
#equivale a x = x ** 2

# //= Atribuição de quociente inteiro da divisão
x//= 2
#equivale a x = x // 2
