# Operadores Aritméticos

# + Adição
print(4 + 2)
#resulta em 6

# - Subtração 
print(4 - 2)
#resulta em 2

# * Multiplicação 
print(4 * 2)
#resulta em 8

# / Divisão 
print(4 / 3)
#resulta em 1.3333

# // Quociente inteiro da divisão
print(4 // 3)
#resulta em 1

# % Resto da divisão inteira 
print(4 % 2)
#resulta em 0

# ** Potenciação 
print(4 ** 2)
#resulta em 16